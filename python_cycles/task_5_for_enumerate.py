"""Розвернути список і вивести індекси
Задача:

Створіть список рядків: ['apple', 'banana', 'cherry', 'date', 'elderberry']
Використовуйте цикл для:
Розвороту списку без використання функції reverse()
Виведення кожного елемента разом з його індексом у розгорнутому списку"""

fruits = ['apple', 'banana', 'cherry', 'date', 'elderberry']

# Проходимо по списку у зворотному порядку
for index, frut in enumerate(fruits[::-1]):  # Використовуємо зріз [::-1]
    print(f"\n{index}: {frut}")