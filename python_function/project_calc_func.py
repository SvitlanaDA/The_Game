"""Виведення документації про функцію"""
print(help(calc))  # Виведе довідку лише один раз перед початком обчислень    

x = input("Будь ласка, введить число: ")

while True:
    operat = (input("Функція (+, -, *, /, **, exit - вихід з програми):  "))
    if operat.lower() == "exit":
        print("Вихід з програми. \nДякуємо, що скористалися нашим калькулятором!")
        break

    y = input("Будь ласка, введіть наступне число: ")
    
    """Виклик функції та оновлення значення x"""
    x = calc(x, y, operat)
    
    """Виведення результату"""
    print(f"{operat} {y} = {x}")