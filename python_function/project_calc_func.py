"""Функція для виконання арифметичних операцій."""

def calc(x, y, operat):

    x, y = float(x), float(y)
    operat = str(operat)
    
    try:           
        if operat == "+":
            return x + y
        if operat == "-":
            return x - y
        if operat == "*":
            return x * y
        if operat == "/":
            return x / y if y != 0 else "Помилка"
        if operat == "**":
            return x ** y
            
        else: 
            return "Помилка: Невідома операція!"
            
    except ZeroDivisionError:
        print("Помилка: Введіть числові значення!")
        return None
    
"""Введення данних"""
