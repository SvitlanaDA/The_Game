"""Вкладений генератор
Задача:

Маючи список списків [[1, 2, 3], [4, 5], [6, 7]], 
використовуйте вкладений генератор списків, 
щоб сплющити список в один список
Виведіть сплющений список"""

# Вирівняти список списків
nested_list = [[1, 2, 3], [4, 5], [6, 7]]
flattened = [item for sublist in nested_list for item in sublist]
print(flattened)